package net.ke8tjm.tle.entities;

public class TLESet {

    private String tleSetName;
    private String tleUrl;
    private String tleText;

    public String getTleSetName() {
        return tleSetName;
    }

    public void setTleSetName(String tleSetName) {
        this.tleSetName = tleSetName;
    }

    public String getTleUrl() {
        return tleUrl;
    }

    public void setTleUrl(String tleUrl) {
        this.tleUrl = tleUrl;
    }

    public String getTleText() {
        return tleText;
    }

    public void setTleText(String tleText) {
        this.tleText = tleText;
    }
}
